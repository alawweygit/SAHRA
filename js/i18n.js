let LANG = localStorage.getItem('hypox_lang') || 'en';
let THEME = localStorage.getItem('hypox_theme') || 'dark';

function t(key) {
  const d = I18N[LANG] || I18N.en;
  return d[key] !== undefined ? d[key] : (I18N.en[key] !== undefined ? I18N.en[key] : key);
}
function tPick(key) {
  const arr = t(key);
  return Array.isArray(arr) ? arr[Math.floor(Math.random() * arr.length)] : arr;
}
function setLang(l) { LANG = l; localStorage.setItem('hypox_lang', l); applyLang(); }
function setTheme(th) { THEME = th; localStorage.setItem('hypox_theme', th); applyTheme(); }
function applyLang() {
  document.documentElement.lang = LANG;
  document.documentElement.dir = I18N[LANG].dir;
  document.body.classList.toggle('ar', LANG === 'ar');
}
function applyTheme() {
  document.body.classList.toggle('theme-light', THEME === 'light');
  document.body.classList.toggle('theme-dark', THEME === 'dark');
}
